package com.practice_exceptionhandling;

public class ExceptionHandling4 
{
	 public static void main(String[] args) 
	    {   
	        try
	        {
	            System.out.println(1);
	        }
	        catch (Exception e) 
	        {
	            System.out.println(2);
	        }
	         
	        //System.out.println(3);
	         
	        finally
	        {
	            System.out.println(4);
	        }
	    }
}
